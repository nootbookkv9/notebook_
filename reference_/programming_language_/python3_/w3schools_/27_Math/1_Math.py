# Math ========================================

# ---------------------------------------------
# Built-in Math Functions

x = min(5, 10, 25)
y = max(5, 10, 25)
print(x)
print(y)

""" Out: 
5
25
"""
# ---------------------------------------------
# The abs() function returns the absolute (positive) value of the specified number:

x = abs(-7.25)
print(x)

""" Out: 
7.25
"""
# ---------------------------------------------
# The pow(x, y) function returns the value of x to the power of y (xy).

x = pow(4, 3)
print(x)

""" Out: 
64
"""
# ---------------------------------------------
# The Math Module

import math
x = math.sqrt(64)
print(x)

""" Out: 
8.0
"""
# ---------------------------------------------
#Import math library
import math

#Round a number upward to its nearest integer
x = math.ceil(1.4)

#Round a number downward to its nearest integer
y = math.floor(1.4)

print(x)
print(y)

""" Out: 
2
1
"""
# ---------------------------------------------
# PI

import math
x = math.pi
print(x)

""" Out: 
3.141592653589793
"""
