# Join Tuples =================================

# ---------------------------------------------
# Join two tuples:

tuple1 = ("a", "b" , "c")
tuple2 = (1, 2, 3)

tuple3 = tuple1 + tuple2
print(tuple3)

""" Out: 
('a', 'b', 'c', 1, 2, 3)
"""
# ---------------------------------------------
# Multiply the fruits tuple by 2:

fruits = ("apple", "banana", "cherry")
mytuple = fruits * 2
print(mytuple)

""" Out: 
('apple', 'banana', 'cherry', 'apple', 'banana', 'cherry')
"""
