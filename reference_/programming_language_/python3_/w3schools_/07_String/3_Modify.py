# Modify Strings ==============================

# ---------------------------------------------
# Upper Case
str = "Hello, World!"
print(str.upper())

# ---------------------------------------------
# Lower Case
str = "Hello, World!"
print(str.lower())

# ---------------------------------------------
# Remove Whitespace
a = " Hello, World! "
print(a.strip()) # returns "Hello, World!" 

# ---------------------------------------------
# Replace String
a = "Hello, World!"
print(a.replace("H", "J"))

# ---------------------------------------------
# Split String
a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!'] 
