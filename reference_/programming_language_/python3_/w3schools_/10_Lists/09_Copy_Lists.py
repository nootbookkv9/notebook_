# Copy Lists ==================================

# ---------------------------------------------
# Copy a List
thislist = ["apple", "banana", "cherry"]
mylist = thislist.copy()
print(mylist)

""" Out: 
['apple', 'banana', 'cherry']
"""
# ---------------------------------------------
# Make a copy of a list with the list() method:
thislist = ["apple", "banana", "cherry"]
mylist = list(thislist)
print(mylist)

""" Out: 
['apple', 'banana', 'cherry']
"""

