#convert from int to float:
x = float(1)

#convert from float to int:
y = int(2.8)

#convert from int to complex:
z = complex(1)

print(x) # Out: 1.0
print(y) # Out: 2
print(z) # Out: (1+0j)

print(type(x)) # Out: <class 'float'>
print(type(y)) # Out: <class 'int'>
print(type(z)) # Out: <class 'complex'> 


''' Note: You cannot convert complex numbers into another number type. '''

