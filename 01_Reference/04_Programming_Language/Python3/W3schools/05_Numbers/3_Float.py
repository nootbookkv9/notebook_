x = 1.10
y = 1.0
z = -35.59

print(type(x)) # Out: <class 'float'>
print(type(y)) # Out: <class 'float'>
print(type(z)) # Out: <class 'float'>


# --------------------------------------------------------------------------------
# Float can also be scientific numbers with an "e" to indicate the power of 10.
# Example

x = 35e3
y = 12E4
z = -87.7e100

print(type(x)) # Out: <class 'float'>
print(type(y)) # Out: <class 'float'>
print(type(z)) # Out: <class 'float'>
