# Slicing Strings =============================
""" Note: The first character has index 0. """

# ---------------------------------------------
# Slice From X to y
str = "Hello, Workd!"
print(str[2:5])

# ---------------------------------------------
# Slice from the start
str = "Hello, Workd!"
print(str[:5])

# ---------------------------------------------
# Slice to the end
str = "Hello, Workd!"
print(str[2:])

# ---------------------------------------------
# Negative indexing
str = "Hello, Workd!"
print(str[-5:-2])

