| # Get Started                                                             |
|---------------------------------------------------------------------------|
| # C++ Comiler Support                                                     |
| https://en.cppreference.com/w/cpp/compiler_support                        |
|---------------------------------------------------------------------------|
| $ c++ --version                     | # This is a compiler				|
| $ gdb --version                     | # This is a debugger				|
|---------------------------------------------------------------------------|
| # Wandbox							  | # Online C++ Editor					|
| # Compiler Explorer				  | # Online C++ Editor					|
| # Coliru							  | # Online C++ Editor					|
|---------------------------------------------------------------------------|
| $ c++ -o app app.cpp                | # Compile c++ file					|
|                                                                           |
| # Note:                                                                   |
| # To compile c++20 you have to add -std=c++20                             |
| # Example:                                                                |
| $ c++ -o app app.cpp -std=c++20                                           |
|___________________________________________________________________________|


-----------------------------------------------------------------------------
| # Library                                                                 |
|---------------------------------------------------------------------------|
| # Note:                                                                   |
| # To import library you have to use --> #include <Library_Name_>          |
|---------------------------------------------------------------------------|
| <iostream>                          | # Standard input-output stream      |
| <string>                            | # String library                    |
| <cmath>                             | # Math library                      |
|_____________________________________|_____________________________________|


-----------------------------------------------------------------------------
| # Using namespace                                                         |
|---------------------------------------------------------------------------|
| using namespace std;                |                                     |
|_____________________________________|_____________________________________|


-----------------------------------------------------------------------------
| # Input and output text                                                   |
|---------------------------------------------------------------------------|
| cout                                | # Output Text                       |
| cin                                 | # Input  Text                       |
| cin >> x;                           | # Input  Text in Variable           |
| getline(cin, x);                    | # Read Full Line                    |
| endl                                | # New line                          |
| \n                                  | # New line                          |
| \t                                  | # Tab                               |
|_____________________________________|_____________________________________|


-----------------------------------------------------------------------------
| # Comments                                                                |
|---------------------------------------------------------------------------|
| //                                  | # Single-line Comment               |
| /* */                               | # Multi-line Comment                |
|_____________________________________|_____________________________________|


-----------------------------------------------------------------------------
| # Variales                                                                |
|---------------------------------------------------------------------------|
| # Syntax:                                                                 |
| # Type Var_Name = Value;                                                  |
|---------------------------------------------------------------------------|
| bool    a = true                    | # 1      Byte                       |
| char    a = 'a'                     | # 1      Byte                       |
| int     a = 5                       | # 2 or 4 Byte                       |
| float   a = 5.0                     | # 4      Byte                       |
| double  a = 5.0                     | # 8      Byte                       |
| string  a = "Text"                  |                                     |
| char  a[] = "Text"                  |                                     |
|---------------------------------------------------------------------------|
| int x = 5, y = 6, z = 50;           | # Declare Multiple Variables        |
|---------------------------------------------------------------------------|
| const float PI = 3.14               | # Constants                         |
|_____________________________________|_____________________________________|


-----------------------------------------------------------------------------
| # String Methods                                                          |
|---------------------------------------------------------------------------|
| str_.length();                      |                                     |
| str_.size();                        |                                     |
| str_[0];                            |                                     |
| str_[str_.length() - 1];            |                                     |
| str_[0] = 'a';                      |                                     |
| str_.at(0);                         |                                     |
| str_.at(str_.length() - 1);         |                                     |
|_____________________________________|_____________________________________|


