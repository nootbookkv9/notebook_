let str = "     Hello World!     ";
let result = str.trimEnd();
console.log(result);
//:      Hello World!

// Description:
// The trimEnd() method removes whitespace from the end of a string.
// The trimEnd() method does not change the original string.
// The trimEnd() method works like trim(), but removes whitespace only from the end of a string.

// Note:
// The trimEnd() method was added to JavaScript in ECMAScript 2019.

// See Also:
// The trim() Method
// The trimStart() Method
// The padEnd() Method
// The padStart() Method

// Syntax:
// string.trimEnd()

// Parameters:
// NONE

// Return:
// A string	A string with removed whitespace from the end of the string.

// History:
// JavaScript String trimEnd() is supported in all browsers since January 2020

