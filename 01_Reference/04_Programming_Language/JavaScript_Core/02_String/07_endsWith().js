// Check if a string ends with "efg":
let str = 'abcdefg';

let result = str.endsWith('efg');

console.log(result);
//: true

// Description:
// The endsWith() method returns true if a string ends with a specified string.
// Otherwise it returns false.
// The endsWith() method is case sensitive.

// See Also:
// The startsWith() Method

// Syntax:
// string.endsWith(searchvalue, length)

// Parameters:
// searchvalue	Required.  // The string to search for.
// length	Optional.  // The length of the string to search.  // Default value is the length of the string.

// Return:
// A boolean	true if the string ends with the value, otherwise false.

// History:
// endsWith() is an ECMAScript6 (ES6) feature.
// ES6 (JavaScript 2015) is supported in all modern browsers since June 2017

