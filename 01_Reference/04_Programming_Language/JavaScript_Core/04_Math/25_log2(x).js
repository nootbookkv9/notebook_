// Examples:
let a = Math.log2(2.7183);
console.log('a',a);
//: 1.4427046851812222

let b = Math.log2(2);
console.log('b',b);
//: 1

let c = Math.log2(1);
console.log('c',c);
//: 0

let d = Math.log2(0);
console.log('d',d);
//: -Infinity

let e = Math.log2(-1);
console.log('e',e);
//: NaN

