// Examples
let a = Math.asinh(0);
console.log('a',a);
//: 0

let b = Math.asinh(1); 
console.log('b',b);
//: 0.881373587019543

// Description:
// The Math.asinh() method returns the hyperbolic arc-sine of a number.

