// Examples:
let x = Math.sqrt(9);
console.log('x',x);
//: 3

let a = Math.sqrt(0);
console.log('a',a);
//: 0

let b = Math.sqrt(1);
console.log('b',b);
//: 1

let c = Math.sqrt(9);
console.log('c',c);
//: 3

let d = Math.sqrt(64);
console.log('d',d);
//: 8

let e = Math.sqrt(-9);
console.log('e',e);
//: NaN

// Description:
// The Math.sqrt() method returns the square root of a number.

// See Also:
// The Math.cbrt() Method
// The Math.SQRT2 Property
// The Math.SQRT1_2 Property

// Syntax:
// Math.sqrt(x)

// Parameters:
// x 	Required.  // A number.

// Return:
	// Number	The square root of the number.
		// NaN if the number negative.

