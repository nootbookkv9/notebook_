// Create a Set
const letters = new Set(["a","b","c"]);
console.log('letters', letters);
//: Set(3) { 'a', 'b', 'c' }

// Description:
// The new Set() constructor creates a Set object.

// Syntax:
// new Set(iterable)

// Parameters:
	// iterable 	Required.
		// An iterable object with values.

// Return:
// Set	A new Set object.

// History:
// Set is an ECMAScript6 (ES6) feature.
// ES6 (JavaScript 2015) is supported in all modern browsers since June 2017:
