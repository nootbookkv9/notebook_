let x1 = Number.NEGATIVE_INFINITY;
console.log(x1);
//: -Infinity

// Create a negative infinity:
let x2 = (-Number.MAX_VALUE) * 2;
console.log(x2);
//: -Infinity

// Description:
// Number.NEGATIVE_INFINITY returns negative infinity.
// Number.NEGATIVE_INFINITY is "a number lower than any other number".

