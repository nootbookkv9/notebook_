// Create a Map
const fruits = new Map();

// Set Map Values
fruits.set("apples", 500);
fruits.set("bananas", 300);
fruits.set("oranges", 200);

console.log('fruits', fruits);
//: Map(3) { 'apples' => 500, 'bananas' => 300, 'oranges' => 200 }

// Description:
// The set() method adds an element to a map.
// The set() method updates an element in a map.

// Syntax:
// map.set(key, value)

// Parameters:
	// key 	Required.
		// The element key.
	// value 	Required.
		// The element value.

// Return:
	// Map	The map object.

