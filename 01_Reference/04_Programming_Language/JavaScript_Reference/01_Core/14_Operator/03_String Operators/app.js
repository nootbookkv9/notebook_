// JavaScript String Operators
// The + operator, and the += operator can also be used to concatenate (add) strings.
// Given that t1 = "Good ", t2 = "Morning", and t3 = "", the table below explains the operators:

// Oper		Example
// +		t3 = t1 + t2
// +=		t1 += t2

// + ============================
let a = "Good ";
let b = "Morning";
let c = a + b;
console.log(c);
//: Good Morning

// += ===========================
let d = "Good ";
let e = "Morning";
d += e;
console.log(d);
//: Good Morning

