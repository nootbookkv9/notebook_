// Create a Set
const letters = new Set(["a","b","c"]);

// Get the Size
let x = letters.size 
console.log('x', x);
//: 3

// Description:
// The size property returns the number of elements in a set.

// Syntax:
// set.size

// Parameters:
// NONE

// Return:
// Number	The number of elements in the set.

// History:
// set.size() is an ECMAScript6 (ES6) feature.
