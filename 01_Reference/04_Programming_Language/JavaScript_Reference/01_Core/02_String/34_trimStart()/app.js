let str = "     Hello World!     ";
let result = str.trimStart();
console.log(result);
//: Hello World!     (--> End Text <--)

// Description:
// The trimStart() method removes whitespace from the beginning of a string.
// The trimStart() method does not change the original string.
// The trimStart() method works like trim(), but removes whitespace only from the start of a string.

// Note:
// The trimStart() method was added to JavaScript in ECMAScript 2019.

// See Also:
// The trim() Method
// The trimEnd() Method
// The padEnd() Method
// The padStart() Method

// Syntax:
// string.trimStart()

// Parameters:
// NONE

// Return:
// A string	A string with removed whitespace from the beginning of the string.

// History:
// JavaScript String trimStart() is supported in all browsers since January 2020
