// Example 1
// Remove (pop) the last element:
let arr = ['a', 'b', 'c'];

arr.pop();

console.log(arr);
//: [ 'a', 'b' ]

// Example 2
// pop() returns the element it removed:
let arr_2 = ['a', 'b', 'c'];

let result = arr_2.pop();

console.log(result);
//: c

// Description:
// The pop() method removes (pops) the last element of an array.
// The pop() method changes the original array.
// The pop() method returns the removed element.

// See Also:
// The Array push() Method
// The Array shift() Method
// The Array unshift() Method

// Syntax:
// array.pop()

// Parameters:
// NONE

// Return:
// A variable	The removed item.
// A string, a number, an array, or any other type allowed in an array.

// History:
// pop() is an ECMAScript1 (ES1) feature.
// ES1 (JavaScript 1997) is fully supported in all browsers
