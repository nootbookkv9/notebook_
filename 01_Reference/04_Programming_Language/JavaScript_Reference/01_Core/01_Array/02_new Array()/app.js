// Create an Array
const cars = new Array(["Saab", "Volvo", "BMW"]);

console.log(cars);
//: [ [ 'Saab', 'Volvo', 'BMW' ] ]

console.log(cars[0])
//: [ 'Saab', 'Volvo', 'BMW' ]

console.log(cars[0][0])
//: Saab

// Description:
// The new Array() constructor creates an Array object.

// Syntax:
// new Array(iterable)

// Parameters:
// iterable 	Required.  // An iterable object with values.

// Return:
// Array	A new Array object.

