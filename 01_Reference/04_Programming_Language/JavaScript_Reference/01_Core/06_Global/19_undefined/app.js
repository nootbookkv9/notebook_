// Variable with no value:
let x;
let text;
if (x === undefined) {
  text = "x is undefined";
} else {
  text = "x is defined";
}
console.log(text);
//: x is undefined

// Description
// The undefined property indicates that a variable has not been assigned a value, or not declared at all.

