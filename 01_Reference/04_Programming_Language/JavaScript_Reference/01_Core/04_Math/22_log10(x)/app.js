// Examples:
let x = Math.log10(2);
console.log('x',x);
//: 0.3010299956639812

let a = Math.log10(2.7183);
console.log('a',a);
//: 0.43429738512450866

let b = Math.log10(2);
console.log('b',b);
//: 0.3010299956639812

let c = Math.log10(1);
console.log('c',c);
//: 0

let d = Math.log10(0);
console.log('d',d);
//: -Infinity

let e = Math.log10(-1);
console.log('e',e);
//: NaN

