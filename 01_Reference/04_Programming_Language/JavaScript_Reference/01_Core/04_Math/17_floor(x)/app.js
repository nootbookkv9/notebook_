// Examples:
let x = Math.floor(1.6);
console.log('x',x);
//: 1

let a = Math.floor(0.60);
console.log('a',a);
//: 0

let b = Math.floor(0.40);
console.log('b',b);
//: 0

let c = Math.floor(5);
console.log('c',c);
//: 5

let d = Math.floor(5.1);
console.log('d',d);
//: 5

let e = Math.floor(-5.1);
console.log('e',e);
//: -6

let f = Math.floor(-5.9);
console.log('f',f);
//: -6

// Description:
// The Math.floor() method rounds a number DOWN to the nearest integer.

