// Examples:
let x = Math.max(5, 10);
console.log('x',x);
//: 10

let a = Math.max(5, 10);
console.log('a',a);
//: 10

let b = Math.max(0, 150, 30, 20, 38);
console.log('b',b);
//: 150

let c = Math.max(-5, 10);
console.log('c',c);
//: 10

let d = Math.max(-5, -10);
console.log('d',d);
//: -5

let e = Math.max(1.5, 2.5);
console.log('e',e);
//: 2.5

// Description:
// The Math.max() method returns the number with the highest value.

// See Also:
// The Math.min() Method

// Syntax:
// Math.max(n1, n2,...)

// Parameters:
// n1, n2,... 	Optional.  // One or more numbers to compare.

// Return:
	// Number	The highest number of the arguments.
		// -Infinity if no arguments are given.
		// NaN if one of the arguments is not a number.

