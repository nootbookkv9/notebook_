// Examples:
let a = Math.ceil(1.4);
console.log('a',a);
//: 2

let b = Math.ceil(0.60);
console.log('b',b);
//: 1

let c = Math.ceil(0.40);
console.log('c',c);
//: 1

let d = Math.ceil(5);
console.log('d',d);
//: 5

let e = Math.ceil(5.1);
console.log('e',e);
//: 6

let f = Math.ceil(-5.1);
console.log('f',f);
//: -5

let g = Math.ceil(-5.9);
console.log('g',g);
//: -5

// Description:
// The Math.ceil() method rounds a number rounded UP to the nearest integer.

