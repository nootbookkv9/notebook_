// Examples
let a = Math.acosh(1);
console.log('a',a);
//: 0

let b = Math.acosh(2);
console.log('b',b);
//: 1.3169578969248166

// Description:
// The Math.acosh() method returns the hyperbolic arc-cosine of a number.
// If the parameter is less than 1, the method returns NaN.

