// Create a Map
const fruits = new Map([
  ["apples", 500],
  ["bananas", 300],
  ["oranges", 200]
]);

// Clear fruits
fruits.clear();

console.log('fruits', fruits);
//: Map(0) {}

// Description:
// The clear() method removes all the elements from a map.

// Syntax:
// map.clear()

// Parameters:
// NONE

// Return:
// NONE

// History:
// map.clear() is an ECMAScript6 (ES6) feature.
