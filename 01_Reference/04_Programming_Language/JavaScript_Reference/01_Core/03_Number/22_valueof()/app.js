// Get the primitive value of a number:
let num = 15;
let result = num.valueOf();
console.log(result);
//: 15

// Description:
// The valueOf() method returns the primitive value of a number.

// Syntax:
// number.valueOf()

// Parameters:
// NONE

// Return:
// A number	The primitive value of the number.

// History:
// valueOf() is an ECMAScript1 (ES1) feature.
// ES1 (JavaScript 1997) is fully supported in all browsers
