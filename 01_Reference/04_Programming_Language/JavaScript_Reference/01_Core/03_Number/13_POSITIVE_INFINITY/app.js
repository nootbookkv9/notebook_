let x1 = Number.POSITIVE_INFINITY;
console.log(x1);
//: Infinity

// Create a positive infinity:
let x2 = (Number.MAX_VALUE) * 2;
console.log(x2);
//: Infinity

// Description:
// POSITIVE_INFINITY returns positive infinity.
// POSITIVE_INFINITY is "something higher than any other number".

